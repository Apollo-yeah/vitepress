  position: relative;
  top: var(--vp-layout-top-height, 0px);
  /*rtl:ignore*/
  left: 0;
  z-index: var(--vp-z-index-nav);
  width: 100%;
  height: var(--vp-nav-height);
  /* pointer-events: none; */

  @media (min-width: 1024px) {
    position: fixed;
  }
}
